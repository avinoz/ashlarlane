window.getAgentBySlug = function (slug) {
  return window.AGENT_ROSTER.find((agent) => agent.slug === slug) || null;
};

window.getAgentProfileUrl = function (slug) {
  const inTeamDir = /\/team(\/|$)/.test(window.location.pathname);
  const prefix = inTeamDir ? "" : "team/";
  return `${prefix}agent.html?slug=${encodeURIComponent(slug)}`;
};

window.getAgentPhotoUrl = function (agent) {
  const file = agent.photo || `${agent.slug}.jpg`;
  const inTeamDir = /\/team(\/|$)/.test(window.location.pathname);
  return `${inTeamDir ? "../" : ""}${file}`;
};

